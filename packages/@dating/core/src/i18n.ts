// ─── App-specific overlay (merge at runtime) ────────────────────────

export function mergeDict(
  lang: Lang,
  appOverrides: Partial<Record<Lang, Record<string, string>>>,
): Record<string, string> {
  return { ...BASE[lang], ...(appOverrides[lang] || {}) }
}

export function t(
  lang: Lang,
  key: string,
  appOverrides?: Partial<Record<Lang, Record<string, string>>>,
): string {
  const dict = mergeDict(lang, appOverrides || {})
  return dict[key] || dict[key.toLowerCase()] || BASE['en'][key] || key
}

export function tPref(lang: Lang, value: string): string {
  return t(lang, value.toLowerCase().replace(/\s+/g, ''))
}

export function tRole(_lang: Lang, role: string): string {
  return role
}

export function getLangLabel(lang: Lang): string {
  return { en: 'EN', tc: '繁', sc: '简', ru: 'RU' }[lang] || 'EN'
}

export function tZodiac(_lang: Lang, sign: string): string {
  return sign
}

export function getDefaultLang(): Lang {
  try {
    const code = (window as any).Telegram?.WebApp?.initDataUnsafe?.user?.language_code || 'en'
    if (code === 'zh-hant' || code === 'zh-hk' || code === 'zh-tw') return 'tc'
    if (code === 'zh-hans' || code === 'zh-cn') return 'sc'
    if (code === 'ru') return 'ru'
    return 'en'
  } catch {
    return 'en'
  }
}
